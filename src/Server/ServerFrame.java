    private void txt_portKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_portKeyTyped
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9')
                || (c == KeyEvent.VK_BACK_SPACE)
                || (c == KeyEvent.VK_DELETE))) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_txt_portKeyTyped

