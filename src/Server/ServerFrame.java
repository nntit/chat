    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        new ThreadServer(new ILog() {
            @Override
            public void Log(String msg) {
                jTextArea1.append(msg + "\n");
                jTextArea1.setCaretPosition(jTextArea1.getDocument().getLength());
            }

            @Override
            public String port() {
                return txt_port.getText();
            }

            @Override
            public String portsys() {
                return txt_portsys.getText();
            }

            @Override
            public void ServerStart(boolean start) {
                if (start) {
                    jPanel1.setBackground(Color.GREEN);
                    jButton1.setEnabled(false);
                    txt_port.setEditable(false);
                    txt_portsys.setEditable(false);
                } else {
                    jPanel1.setBackground(Color.RED);
                    jButton1.setEnabled(true);
                    txt_port.setEditable(true);
                    txt_portsys.setEditable(false);
                }
            }
        });
    }//GEN-LAST:event_jButton1ActionPerformed

